/// A stable, content-addressed identifier for a ComputeImage.
#[derive(Debug, Clone, Hash, Eq, PartialEq, Serialize, Deserialize)]
pub struct ComputeImageId(pub String);

/// Identifier for a target hardware profile (e.g. "apple-m1-pro-16", "apple-m4-max-64").
#[derive(Debug, Clone, Hash, Eq, PartialEq, Serialize, Deserialize)]
pub struct TargetProfileId(pub String);

